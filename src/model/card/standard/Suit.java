package model.card.standard;

/**
 * The Suit enum represents the four suits of a standard deck of playing cards.
 */
public enum Suit {
    /**
     * The HEART suit.
     */
    HEART,

    /**
     * The DIAMOND suit.
     */
    DIAMOND,

    /**
     * The SPADE suit.
     */
    SPADE,

    /**
     * The CLUB suit.
     */
    CLUB
}